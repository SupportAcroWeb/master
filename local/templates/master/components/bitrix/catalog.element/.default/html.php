                <div class="textblock1">
                    <h2 class="title6">Описание</h2>
                    <p>Мы постоянно сталкиваемся с проблемами обустройства проемов на стенах, оклеиваемых плиткой, панелями, зеркалами, мозаикой и подобными им материалами. Проблемы обычно заключается в том, что необходимо разместить проем в нужном месте на стене и при этом совсем не хочется нарушать рисунок на плитке или мозаике. В таких случаях к месту придется наша новая разработка – люк Универсал.</p>
                    <p>Его особенность в том, что проем под установку люка можно размещать в любом месте стены, не подбирая его под размеры плитки, межплиточных швов, и т.д. Секрет до гениального прост. Крышка люка оснащена углублением, в котором может быть размещен наклеиваемый материал. При этом вклеенные внутрь крышки фрагменты отделки остаются обрамленными по периметру металлической рамкой. Это позволяет снимать крышку люка с места крепления не подвергая отделочные материалы на ней риску быть сколотыми.</p>
                    <p>Люк полностью организован как самостоятельное изделие и позволяет вписать его в любое место на отделке не нарушая общего замысла рисунка плитки, мозаики и т.п. Ко всем остальным достоинствам люка Универсал следует добавить и тот факт, что он значительно дешевле всех известных моделей люков под плитку.</p>
